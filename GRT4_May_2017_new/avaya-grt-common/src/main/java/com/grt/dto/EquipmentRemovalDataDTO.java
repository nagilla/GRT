package com.grt.dto;

import java.io.Serializable;
import java.util.List;


public class EquipmentRemovalDataDTO implements Serializable{
	private static final long serialVersionUID = 1L;

	private String registrationId;
	private String errorId;
	private String routingInfo;
	private List<EquipmentRemovalResponseDto> equipmentRemovalResponseData;
	/**
	 * @return the equipmentRemovalResponseDto
	 */
	public List<EquipmentRemovalResponseDto> getEquipmentRemovalResponseDto() {
		return equipmentRemovalResponseData;
	}
	/**
	 * @param equipmentRemovalResponseDto the equipmentRemovalResponseDto to set
	 */
	public void setEquipmentRemovalResponseDto(
			List<EquipmentRemovalResponseDto> equipmentRemovalResponseData) {
		this.equipmentRemovalResponseData = equipmentRemovalResponseData;
	}
	/**
	 * @return the errorId
	 */
	public String getErrorId() {
		return errorId;
	}
	/**
	 * @param errorId the errorId to set
	 */
	public void setErrorId(String errorId) {
		this.errorId = errorId;
	}
	/**
	 * @return the registrationId
	 */
	public String getRegistrationId() {
		return registrationId;
	}
	/**
	 * @param registrationId the registrationId to set
	 */
	public void setRegistrationId(String registrationId) {
		this.registrationId = registrationId;
	}
	public String getRoutingInfo() {
		return routingInfo;
	}
	public void setRoutingInfo(String routingInfo) {
		this.routingInfo = routingInfo;
	}
	
}
