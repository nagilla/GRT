package com.grt.dto;

import java.io.Serializable;



public class SALGateway implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String soldTo, materialCode, materialDescription, status, seCode, seid;
	private String gatewayCreatedDate;
	private Long devicesBehindGateway;
	private String primaryOrSecondary;
	private boolean primaryChecked = false;
	private boolean secondaryChecked = false;
	
	public Long getDevicesBehindGateway() {
		return devicesBehindGateway;
	}
	public void setDevicesBehindGateway(Long devicesBehindGateway) {
		this.devicesBehindGateway = devicesBehindGateway;
	}
	public String getGatewayCreatedDate() {
		return gatewayCreatedDate;
	}
	public void setGatewayCreatedDate(String gatewayCreatedDate) {
		this.gatewayCreatedDate = gatewayCreatedDate;
	}
	public String getMaterialCode() {
		return materialCode;
	}
	public void setMaterialCode(String materialCode) {
		this.materialCode = materialCode;
	}
	public String getMaterialDescription() {
		return materialDescription;
	}
	public void setMaterialDescription(String materialDescription) {
		this.materialDescription = materialDescription;
	}
	public String getSeCode() {
		return seCode;
	}
	public void setSeCode(String seCode) {
		this.seCode = seCode;
	}
	public String getSeid() {
		return seid;
	}
	public void setSeid(String seid) {
		this.seid = seid;
	}
	public String getSoldTo() {
		return soldTo;
	}
	public void setSoldTo(String soldTo) {
		this.soldTo = soldTo;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getPrimaryOrSecondary() {
		return primaryOrSecondary;
	}
	public void setPrimaryOrSecondary(String primaryOrSecondary) {
		this.primaryOrSecondary = primaryOrSecondary;
	}
	public boolean isPrimaryChecked() {
		return primaryChecked;
	}
	public void setPrimaryChecked(boolean primaryChecked) {
		this.primaryChecked = primaryChecked;
	}
	public boolean isSecondaryChecked() {
		return secondaryChecked;
	}
	public void setSecondaryChecked(boolean secondaryChecked) {
		this.secondaryChecked = secondaryChecked;
	}

	
}
