package com.grt.dto;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class TechRegAlarmInputDto implements Serializable {
	
	private static final long serialVersionUID = 1L;
	
	private String grtId;
	private String soldTo;
	private List<TechRegInputDto>  techRegInputDtoList;
	
	public String getGrtId() {
		return grtId;
	}
	
	public void setGrtId(String grtId) {
		this.grtId = grtId;
	}
	
	public String getSoldTo() {
		return soldTo;
	}
	
	public void setSoldTo(String soldTo) {
		this.soldTo = soldTo;
	}
	
	public List<TechRegInputDto> getTechRegInputDtoList() {
		return techRegInputDtoList;
	}
	
	public void setTechRegInputDtoList(List<TechRegInputDto> techRegInputDtoList) {
		if ( techRegInputDtoList == null) {
			techRegInputDtoList = new ArrayList();
		}
		
		this.techRegInputDtoList = techRegInputDtoList;
	}
	    
		
}
